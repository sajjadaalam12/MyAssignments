package week1.day2;

public class FindOddOrEvenNumbers {

	public static void main(String[] args) {
		
		for (int i=0;i<=15;i++) {
			
			if(i%2!=0) {
				System.out.println("Odd numbers from 1 to 15:  " +i);
			}
			
			else {
				System.out.println("Even numbers from 1 to 15:  " +i);
			}
		}

	}

}
